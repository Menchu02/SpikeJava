package poo.ejercicioOpendBootcamp;

public class SmartDevice {
    //Atributos Dispositivo inteligente
    String name;
    Boolean wifi;
    double capacidad;


    //CONSTRUCTOR
    public SmartDevice() {
    }

    public SmartDevice(String name, Boolean wifi, double capacidad) {
        this.name = name;
        this.wifi = wifi;
        this.capacidad = capacidad;
    }
}
